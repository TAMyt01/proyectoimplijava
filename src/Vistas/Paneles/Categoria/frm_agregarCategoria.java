    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtNombre.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(null, "el campo nombre es obligatorio");
        } else {
            ctrlCategoria ccontrolCategoria = new ctrlCategoria();
            if (!ccontrolCategoria.existeCategoria(txtNombre.getText())) {
                clsCategoria cate = new clsCategoria();
                cate.setNombre(txtNombre.getText());
                cate.setDescripcion(txtDesc.getText());

                if (ccontrolCategoria.guardar()) {
                    JOptionPane.showMessageDialog(null, "Registro guardado");
                    panelcategoria.cargaTablaCategoria();//Actualizamos tabla del panel principal de la categoria
                } else {
                    JOptionPane.showMessageDialog(null, "Error al guardar");
                }

                txtNombre.setText("");
                txtDesc.setText("");
            }else{
                JOptionPane.showMessageDialog(null,ccontrolCategoria.mens);
            } 
                

            

        }


    }//GEN-LAST:event_btnGuardarActionPerformed

