    private void cargaTablaCategoria() {
        Connection cn = clsConexion.conectar();

        String sql = "SELECT p.nombre AS nombre_producto, "
           + "dv.precioUnitario, "
           + "dv.cantidad, "
           + "(dv.precioUnitario * dv.cantidad) AS subtotal_total "
           + "FROM tb_detalle_venta dv "
           + "JOIN tb_producto p ON dv.idProducto = p.ID_Producto "
           + "WHERE dv.idCabeceraVenta = ? "
           + "GROUP BY p.nombre, dv.precioUnitario, dv.cantidad";

        try {
            PreparedStatement consulta = cn.prepareStatement(sql);
            consulta.setInt(1, ID);
            System.out.println("ID recibido: " + ID);
            ResultSet rs = consulta.executeQuery();
            rellenarTabla(rs);
            
        } catch (SQLException ex) {
            System.out.println("Error al actualizar la tabla de historial: " + ex);
        }
    }

    private void rellenarTabla(ResultSet rs) throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tablas.getModel();
        model.setRowCount(0);
        while (rs.next()) {
            Object fila[] = new Object[4];
            for (int i = 0; i < 4; i++) {
                fila[i] = rs.getObject(i + 1);
            }
            model.addRow(fila);
        }
    }

    private void sumar() {
        Connection cn = clsConexion.conectar();

        String sql = "SELECT valorPagar from tb_cabecera_venta where idCabeceraVenta=?";

        try {
            PreparedStatement consulta = cn.prepareStatement(sql);
            consulta.setInt(1, ID);
            System.out.println("ID recibido: " + ID);
            ResultSet rs = consulta.executeQuery();
            
            while(rs.next()){
                 txtTotal.setText(rs.getDouble("valorPagar")+"");
            }
            
          
            
        } catch (SQLException ex) {
            System.out.println("Error en la actualizar suma: " + ex);
        }
    
    }

